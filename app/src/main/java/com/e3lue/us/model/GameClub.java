public class GameClub {
    private String ClubName;
    private String ResponsePerson;
    private String ResponsePersonPhone;
    private String Longitude;
    private String Latitude;
    private String Address;

    public String getClubName() {
        return ClubName;
    }

    public void setClubName(String clubName) {
        ClubName = clubName;
    }

    public String getResponsePerson() {
        return ResponsePerson;
    }

    public void setResponsePerson(String responsePerson) {
        ResponsePerson = responsePerson;
    }

    public String getResponsePersonPhone() {
        return ResponsePersonPhone;
    }

    public void setResponsePersonPhone(String responsePersonPhone) {
        ResponsePersonPhone = responsePersonPhone;
    }

    public String getLongitude() {
        return Longitude;
    }

    public void setLongitude(String longitude) {
        Longitude = longitude;
    }

    public String getLatitude() {
        return Latitude;
    }

    public void setLatitude(String latitude) {
        Latitude = latitude;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }



}
